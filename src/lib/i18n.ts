const TRANSLATIONS: Record<LangCode, Translations> = { en, de, fr, es, pt, nl, sv, ja };

export function getTranslations(lang: string): Translations {
  return TRANSLATIONS[lang as LangCode] ?? TRANSLATIONS.en;
}

export function t(translations: Translations, key: string, vars?: Record<string, string | number>): string {
  const parts = key.split('.');
  let value: unknown = translations;
  for (const part of parts) {
    if (value && typeof value === 'object') {
      value = (value as Record<string, unknown>)[part];
    } else {
      value = undefined;
      break;
    }
  }
  let str = typeof value === 'string' ? value : key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(`{${k}}`, String(v));
    }
  }
  return str;
}
